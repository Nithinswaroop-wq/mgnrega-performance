package com.example.Mgnrega.DTO;

public class PerformanceData
{
    private String district;
    private String jobCardsIssued;
    private String totalWorkers;
    private String activeWorkers;
    private String approvedLabourBudget;
    private String personDays;
    private String womenWorkers;
    private String completedWorks;
    private String ongoingWorks;


    // Getters and setters
    public String getDistrict() {
        return district;
    }

    public void setDistrict(String district) {
        this.district = district;
    }

    public String getJobCardsIssued() {
        return jobCardsIssued;
    }

    public void setJobCardsIssued(String jobCardsIssued) {
        this.jobCardsIssued = jobCardsIssued;
    }

    public String getTotalWorkers() {
        return totalWorkers;
    }

    public void setTotalWorkers(String totalWorkers) {
        this.totalWorkers = totalWorkers;
    }

    public String getActiveWorkers() {
        return activeWorkers;
    }

    public void setActiveWorkers(String activeWorkers) {
        this.activeWorkers = activeWorkers;
    }

    public String getApprovedLabourBudget() {
        return approvedLabourBudget;
    }

    public void setApprovedLabourBudget(String approvedLabourBudget) {
        this.approvedLabourBudget = approvedLabourBudget;
    }

    public String getPersonDays() {
        return personDays;
    }

    public void setPersonDays(String personDays) {
        this.personDays = personDays;
    }

    public String getWomenWorkers() {
        return womenWorkers;
    }

    public void setWomenWorkers(String womenWorkers) {
        this.womenWorkers = womenWorkers;
    }

    public String getCompletedWorks() {
        return completedWorks;
    }

    public void setCompletedWorks(String completedWorks) {
        this.completedWorks = completedWorks;
    }

    public String getOngoingWorks() {
        return ongoingWorks;
    }

    public void setOngoingWorks(String ongoingWorks) {
        this.ongoingWorks = ongoingWorks;
    }

}
